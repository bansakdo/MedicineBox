package medicine;

public class User {
	
	private String user_id;
	private String user_pwd;
	private String user_name;
	private String user_phone;
	private String user_device;
	private int user_alarm;
	
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getUser_pwd() {
		return user_pwd;
	}
	public void setUser_pwd(String user_pwd) {
		this.user_pwd = user_pwd;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public String getUser_phone() {
		return user_phone;
	}
	public void setUser_phone(String user_phone) {
		this.user_phone = user_phone;
	}
	public String getUser_device() {
		return user_device;
	}
	public void setUser_device(String user_device) {
		this.user_device = user_device;
	}
	public int getUser_alarm() {
		return user_alarm;
	}
	public void setUser_alarm(int user_alarm) {
		this.user_alarm = user_alarm;
	}
	
	
	

}
