package medicine;

public class Medi {
	private int medi_num;
	private String medi_name;
	private String medi_photo;
	private String medi_effect;
	private String medi_use;
	private int medi_store;
	private int medi_search;
	
	public int getMedi_num() {
		return medi_num;
	}
	public void setMedi_num(int medi_num) {
		this.medi_num = medi_num;
	}
	public String getMedi_name() {
		return medi_name;
	}
	public void setMedi_name(String medi_name) {
		this.medi_name = medi_name;
	}
	public String getMedi_photo() {
		return medi_photo;
	}
	public void setMedi_photo(String medi_photo) {
		this.medi_photo = medi_photo;
	}
	public String getMedi_effect() {
		return medi_effect;
	}
	public void setMedi_effect(String medi_effect) {
		this.medi_effect = medi_effect;
	}
	public String getMedi_use() {
		return medi_use;
	}
	public void setMedi_use(String medi_use) {
		this.medi_use = medi_use;
	}
	public int getMedi_store() {
		return medi_store;
	}
	public void setMedi_store(int medi_store) {
		this.medi_store = medi_store;
	}
	public int getMedi_search() {
		return medi_search;
	}
	public void setMedi_search(int medi_search) {
		this.medi_search = medi_search;
	}
	
	
	
	
}
