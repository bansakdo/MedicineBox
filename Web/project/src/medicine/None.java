package medicine;

public class None {
	
	private int none_num;
	private String none_name;
	private int none_store;
	private int none_search;
	
	public int getNone_num() {
		return none_num;
	}
	public void setNone_num(int none_num) {
		this.none_num = none_num;
	}
	public String getNone_name() {
		return none_name;
	}
	public void setNone_name(String none_name) {
		this.none_name = none_name;
	}
	public int getNone_store() {
		return none_store;
	}
	public void setNone_store(int none_store) {
		this.none_store = none_store;
	}
	public int getNone_search() {
		return none_search;
	}
	public void setNone_search(int none_search) {
		this.none_search = none_search;
	}
	
	
}
